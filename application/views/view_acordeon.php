                        <h4>Los problemas psicológicos más frecuentes en los niños son:</h4>
                        <ul class="list-group list-group-flush">
                            <li class="list-group-item text-justify"><b>Acoso escolar o bullying:</b> Es el abuso o maltrato, sin motivo evidente, que sufre un menor intencionada y repetidamente por parte de uno o varios compañeros del ámbito académico. Puede ser maltrato físico, psíquico, social o verbal.</li>
                            <li class="list-group-item text-justify"><b>Amigos imaginarios:</b> A pesar de ser un fenómeno que enriquece la imaginación y la interacción social, es importante observar la evolución del problema. Los “amigos imaginarios” suelen ser casos idealizados (duendes, hadas y superhéroes) o amistades invisibles con las que compartir sus juegos.</li>
                            <li class="list-group-item text-justify"><b>Fobia escolar:</b> Es la incapacidad total o parcial del niño de acudir al colegio a causa de un miedo irracional sobre algún aspecto de la situación escolar que le crea un nivel de ansiedad elevado.</li>
                            <li class="list-group-item text-justify"><b>Autismo:</b> Es un trastorno neuropsiquiátrico que se caracteriza por el aislamiento social, las dificultades en la comunicación y los patrones estereotipados de conducta.</li>
                            <li class="list-group-item text-justify"><b>Depresión infantil:</b> Es un trastorno que se caracteriza por una alteración en el estado de ánimo en el menor que puede llegar a sumirse en un estado de tristeza profundo o padecer bruscos cambios de humor.</li>
                            <li class="list-group-item text-justify"><b>Divorcio de los progenitores:</b> El divorcio de los padres provoca en el menor un impacto emocional, que puede provocar diferentes actitudes que afecten a distintos ámbitos de su alrededor: sociabilidad, autoestima, estudio, comportamiento, etc.</li>
                            <li class="list-group-item text-justify"><b>Tics nerviosos:</b> Son movimientos involuntarios bruscos, cortos y repetitivos de cualquier grupo muscular. Las causas de este trastorno pueden ser físicas o psicológicas y, en consecuencia, pueden provocar problemas conductuales y sociales. Generalmente, este trastorno desaparece a los meses.</li>
                            <li class="list-group-item text-justify"><b>Trastorno por déficit de atención e hiperactividad infantil (TDAH):</b> Es un trastorno del comportamiento que se caracteriza por distracción, períodos de atención transitoria, inquietud e inestabilidad emocional, hiperactividad y conductas impulsivas.</li>
                        </ul>

